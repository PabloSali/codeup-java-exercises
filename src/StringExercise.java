public class StringExercise {
    public static void main(String[] args) {
        String str = "We don't need no education";
        System.out.println(str);
        str = " We don't need no thought control";
        System.out.println(str);
        str = " Check \"this\" out!, \"s inside of \"s!";
        System.out.println(str);
        str = " In windows, the main drive is usually C:\\\n";
        System.out.println(str);
        str = " I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!\n";
        System.out.println(str);
    }
}
